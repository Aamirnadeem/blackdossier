package com.packetnode.blackdossier.quote;

public class QuoteFactoryException extends Exception
{

	public QuoteFactoryException()
	{
	}

	public QuoteFactoryException(String message)
	{
		super(message);
	}

	public QuoteFactoryException(Throwable cause)
	{
		super(cause);
	}

	public QuoteFactoryException(String message, Throwable cause)
	{
		super(message, cause);
	}

}
